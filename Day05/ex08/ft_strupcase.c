#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] -= 32;
		i++;
	}
	return (str);
}

int main(void)
{
	char str1[] = "good morning";

	printf("%s\n", ft_strupcase(str1));
	return (0);
}
